#!/usr/bin/env node
// Generate RELEASE-EVIDENCE.md (ADR-025) from live repo state, so the dogfooding
// evidence cannot drift into fiction. It reports the real autonomy posture, which
// gates pass, the AgentProof score, promoted learnings, and the work queue. Run with
// --check to fail if the committed file is stale.
//
// Usage:
//   node scripts/build-release-evidence.mjs            write RELEASE-EVIDENCE.md
//   node scripts/build-release-evidence.mjs --check    fail if it is out of date
import { readFileSync, writeFileSync, readdirSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { spawnSync } from "node:child_process";
import { parseFlatYaml } from "./lib/yaml-lite.mjs";
import { readPromotedLearnings } from "./lib/learnings.mjs";

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");
const check = process.argv.includes("--check");

function gate(script, args = []) {
  const r = spawnSync("node", [join(root, "scripts", script), ...args], { encoding: "utf8", cwd: root, timeout: 60000 });
  return r.status === 0;
}
function mark(ok) { return ok ? "pass" : "FAIL"; }

const cfg = parseFlatYaml(readFileSync(join(root, ".modonome", "config.yaml"), "utf8"));
const version = readFileSync(join(root, ".modonome", "version"), "utf8").trim();

const gates = [
  ["Drift guard", gate("check-drift.mjs")],
  ["Style check", gate("check-style.mjs", ["."])],
  ["Repo hygiene", gate("check-repo-hygiene.mjs")],
  ["Self-application conformance", gate("check-self-application.mjs")],
  ["Learning traceability", gate("check-learning-traceability.mjs")],
  ["Capability promotion readiness", gate("check-promotion-readiness.mjs")],
  ["Work item validation", gate("check-work-items.mjs")],
  ["Checker engagement", gate("check-checker-engagement.mjs")],
];

const ap = spawnSync("node", [join(root, "agentproof/runner.mjs"), "--json"], { encoding: "utf8", cwd: root, timeout: 60000 });
let apScore = "unavailable";
try { apScore = JSON.parse(ap.stdout).score; } catch { /* leave unavailable */ }
const apLevel = ap.status === 0 ? "HARDENED" : (apScore === "unavailable" ? "unavailable" : "UNHARDENED");

const wiDir = join(root, ".modonome", "work-items");
const byState = {};
if (existsSync(wiDir)) {
  for (const f of readdirSync(wiDir).filter((f) => f.endsWith(".json"))) {
    const s = JSON.parse(readFileSync(join(wiDir, f), "utf8")).state;
    byState[s] = (byState[s] || 0) + 1;
  }
}
const learnings = readPromotedLearnings(root);

// Sample-app captures: real maker and checker runs recorded under examples/<app>/runs/.
// These directories are committed (unlike the gitignored .modonome/runs/), so summarizing
// them stays reproducible from committed state. Each summary is read from the captured
// metrics.jsonl, never hand-authored.
function listCaptures() {
  const out = [];
  const exDir = join(root, "examples");
  if (!existsSync(exDir)) return out;
  for (const app of readdirSync(exDir)) {
    const runsDir = join(exDir, app, "runs");
    if (!existsSync(runsDir)) continue;
    for (const ts of readdirSync(runsDir)) {
      const metricsPath = join(runsDir, ts, "metrics.jsonl");
      if (!existsSync(metricsPath)) continue;
      const events = readFileSync(metricsPath, "utf8").split("\n").filter((l) => l.trim()).map((l) => JSON.parse(l));
      const maker = events.find((e) => e.event === "maker_run") || {};
      const checker = events.find((e) => e.event === "checker_review") || {};
      out.push({
        app, ts,
        makerModel: maker.maker_model || "unknown",
        checkerModel: checker.checker_model || "unknown",
        questions: Number(checker.checker_questions_raised) || 0,
        requestedChanges: checker.checker_requested_changes === true,
      });
    }
  }
  return out.sort((a, b) => (a.ts < b.ts ? 1 : -1));
}
const captures = listCaptures();

const armed = cfg.autonomy_enabled === true && cfg.dry_run !== true;

const lines = [];
lines.push("# Release evidence");
lines.push("");
lines.push("Generated by `scripts/build-release-evidence.mjs` from live repo state (ADR-025).");
lines.push("Regenerate with `npm run evidence`. Do not hand-edit.");
lines.push("");
lines.push(`- Version: ${version}`);
lines.push(`- Autonomy posture: ${armed ? "ARMED" : "dry-run / off"} (autonomy_enabled=${cfg.autonomy_enabled}, dry_run=${cfg.dry_run}, auto_merge=${cfg.auto_merge})`);
lines.push("");
lines.push("## What is proven today");
lines.push("");
lines.push("The engine has not run in armed mode on this repository. The evidence below is the");
lines.push("deterministic governance layer applied to Modonome itself: the gates that run in CI,");
lines.push("the conformance suite, and the traceable learning record. Armed-mode transcripts will");
lines.push("be added here once the owner arms the loop on this repo.");
lines.push("");
lines.push("## Gate results");
lines.push("");
lines.push("| Gate | Result |");
lines.push("|---|---|");
for (const [name, ok] of gates) lines.push(`| ${name} | ${mark(ok)} |`);
lines.push(`| AgentProof | ${apScore} ${apLevel} |`);
lines.push("");
lines.push("## Work queue");
lines.push("");
if (Object.keys(byState).length === 0) {
  lines.push("No work items.");
} else {
  for (const [s, n] of Object.entries(byState).sort()) lines.push(`- ${s}: ${n}`);
}
lines.push("");
// Note: per-run dry-run transcripts live in the gitignored .modonome/runs/
// directory and carry per-invocation timestamps. They are deliberately excluded
// here so this committed file stays reproducible from committed repo state.
// Dry-run and armed-mode sweep detail belongs in a point-in-time release
// transcript, not in the continuously-regenerated evidence file (see ADR-025).
lines.push("## Sample-app captures");
lines.push("");
if (captures.length === 0) {
  lines.push("No captured sample-app runs recorded yet.");
} else {
  lines.push("Real maker and checker runs recorded on the sample apps. Each is captured output,");
  lines.push("read from the committed metrics, not hand-authored. The maker and checker use distinct");
  lines.push("models, and the run directory holds the verbatim logs.");
  lines.push("");
  for (const c of captures) {
    const decision = c.requestedChanges ? "requested changes" : "approved";
    lines.push(`- examples/${c.app}/runs/${c.ts}: maker ${c.makerModel}, checker ${c.checkerModel}; checker ${decision} and raised ${c.questions} question(s).`);
  }
}
lines.push("");
lines.push("## Promoted learnings (traceable)");
lines.push("");
if (learnings.length === 0) {
  lines.push("None promoted yet.");
} else {
  for (const l of learnings) lines.push(`- ${l.id}: ${l.lesson} (gate: ${l.gate_location}; signal: ${l.correction_signal_id})`);
}
lines.push("");

const out = lines.join("\n");
const path = join(root, "RELEASE-EVIDENCE.md");

if (check) {
  const current = existsSync(path) ? readFileSync(path, "utf8") : "";
  if (current.trim() !== out.trim()) {
    console.error("RELEASE-EVIDENCE.md is out of date. Run: npm run evidence");
    process.exit(1);
  }
  console.log("RELEASE-EVIDENCE.md is current.");
} else {
  writeFileSync(path, out + "\n");
  console.log(`Wrote ${path}`);
}
