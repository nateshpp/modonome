/**
 * detect-attribution.mjs
 *
 * Deterministic detection of AI-participation signatures across the surfaces where
 * they leak: branch names, commit author/committer identity, commit-message bodies
 * (trailers), and free text (PR bodies, comments, tracked files). It consolidates the
 * three existing detectors (branch-name, commit-identity, and the house-style
 * AI-signature rule) into one structured report and adds the coverage they lacked:
 * every path segment of a branch (not only the first) and trailer lines inside commit
 * bodies (not only the author identity fields).
 *
 * This is the CHECK half of Governed Remediation. It is a pure library plus a small
 * CLI wrapper: it computes findings and a precomputed remedy, but never mutates the
 * repository. It holds no secrets and makes no network call, so it runs from the
 * base-branch trust root in CI with no token, and locally with zero infrastructure.
 *
 * The detector is loaded from the base branch on a PR (see ci.yml). Keeping it here,
 * alongside branch-name.mjs and commit-identity.mjs which it reuses, means the whole
 * attribution trust boundary is one base-pinned kernel a PR cannot weaken from within.
 */

import { DENYLISTED_PREFIXES, isModelIdentifierBranch } from "./branch-name.mjs";
import { findForbiddenCommits, isForbiddenIdentity } from "./commit-identity.mjs";

// Single source of truth for the AI-authorship-signature pattern. check-style.mjs
// imports this so the house-style linter and the detector can never drift apart.
// Matches: co-author trailers, "generated by <model>", the model-language cliche,
// the robot-emoji "generated with" footer, and claude.ai/code session URLs.
export const AI_SIGNATURE_RE =
  /\b(co-authored-by|generated by (claude|gpt|an ai)|as an ai language model)\b|\u{1F916}\s*generated with|claude\.ai\/code/iu;

/**
 * True when any path segment of a branch name exactly equals a denylisted token.
 * This is a strict superset of isModelIdentifierBranch (which checks only the first
 * segment): it also catches evasions like "feature/claude/adapter" where the model
 * identifier hides in a later segment. It stays exact-match per segment, so a
 * descriptive name such as "feature/ai-adapter" (segment "ai-adapter", not "ai") is
 * still allowed. Substring and separator variants are Phase 1's widener territory,
 * gated by a false-positive corpus.
 */
export function branchHasModelSegment(name) {
  if (!name || typeof name !== "string") return false;
  return name
    .split("/")
    .some((seg) => DENYLISTED_PREFIXES.includes(seg.trim().toLowerCase()));
}

/**
 * Propose a compliant branch name by replacing any denylisted segment with a neutral
 * placeholder, preserving the rest of the path so the suggestion stays meaningful.
 * "claude/fix-config" -> "change/fix-config"; "feature/ai/x" -> "feature/change/x".
 * Callers with richer context (the diff) can override this; it is the zero-context
 * default the block message offers so a human never has to invent a name.
 */
export function suggestBranchName(name) {
  if (!name || typeof name !== "string") return "change/update";
  const segments = name.split("/").map((seg) => {
    const bare = seg.trim().toLowerCase();
    return DENYLISTED_PREFIXES.includes(bare) ? "change" : seg;
  });
  const joined = segments.join("/");
  // If the leading segment was the only content, keep a descriptive shape.
  return joined === "change" ? "change/update" : joined;
}

/**
 * Scan a branch name and return a finding if it carries a model identifier.
 */
export function detectBranch(name) {
  if (!branchHasModelSegment(name)) return null;
  return {
    kind: "branch",
    where: name,
    pattern: "model-identifier-segment",
    matched: name,
    remedy: {
      action: "rename-branch",
      suggestion: suggestBranchName(name),
    },
  };
}

/**
 * Scan commits for forbidden author/committer identity (reusing commit-identity.mjs)
 * AND for AI signatures inside commit-message bodies. The identity check and the body
 * check are complementary: the first catches "Committed by Claude", the second catches
 * a human-authored commit whose message ends with a "Co-Authored-By: Claude" trailer.
 *
 * @param {string} logOutput tab-delimited `an\tae\tcn\tce\tsha` lines (identity scan)
 * @param {Array<{sha:string, body:string}>} bodies optional commit bodies (trailer scan)
 */
export function detectCommits(logOutput, bodies = []) {
  const findings = [];
  for (const offender of findForbiddenCommits(logOutput)) {
    findings.push({
      kind: "commit-identity",
      where: offender.sha,
      pattern: "agent-identity",
      matched: `author: ${offender.author}, committer: ${offender.committer}`,
      remedy: { action: "reauthor-commit", suggestion: "re-author with a human or project identity" },
    });
  }
  for (const { sha, body } of bodies) {
    if (body && AI_SIGNATURE_RE.test(body)) {
      findings.push({
        kind: "commit-body",
        where: sha || "(working tree)",
        pattern: "ai-signature-trailer",
        matched: firstMatch(body),
        remedy: { action: "rewrite-message", suggestion: "remove the attribution trailer from the commit message" },
      });
    }
  }
  return findings;
}

/**
 * Scan a block of free text (PR body, a comment, a tracked file) for AI signatures.
 * Returns one finding per matching line so the remedy can point at the exact spot.
 */
export function detectText(kind, where, text) {
  const findings = [];
  const lines = (text || "").split("\n");
  lines.forEach((line, i) => {
    if (AI_SIGNATURE_RE.test(line)) {
      findings.push({
        kind,
        where: `${where}:${i + 1}`,
        pattern: "ai-signature",
        matched: line.trim(),
        remedy: { action: "redact-line", suggestion: "remove the AI authorship signature from this line" },
      });
    }
  });
  return findings;
}

function firstMatch(text) {
  const m = (text || "").match(AI_SIGNATURE_RE);
  return m ? m[0] : "";
}

/**
 * Render a precomputed, actionable remedy so a blocked violation is never a dead end.
 * The message names the exact fix and, where applicable, the literal git commands to
 * apply it, so a reviewer pastes rather than puzzles.
 */
export function formatRemedy(findings) {
  if (!findings.length) return "No AI-participation signatures found.";
  const lines = [`Found ${findings.length} AI-participation signature(s):`, ""];
  for (const f of findings) {
    lines.push(`  [${f.kind}] ${f.where}`);
    lines.push(`    matched: ${f.matched}`);
    if (f.remedy.action === "rename-branch") {
      lines.push(`    fix:     git branch -m ${f.remedy.suggestion} && git push origin -u ${f.remedy.suggestion}`);
    } else if (f.remedy.action === "reauthor-commit") {
      lines.push(`    fix:     ${f.remedy.suggestion} (git commit --amend --reset-author, or rebase)`);
    } else {
      lines.push(`    fix:     ${f.remedy.suggestion}`);
    }
    lines.push("");
  }
  return lines.join("\n");
}

// Re-export the reused predicates so callers can depend on this one kernel module.
export { isModelIdentifierBranch, isForbiddenIdentity, DENYLISTED_PREFIXES };
